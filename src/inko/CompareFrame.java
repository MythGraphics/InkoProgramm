    private void showStatus(int linesChanged) {
        if (linesChanged > 0) {
            statusField.showSuccess( linesChanged + " Zeilen verarbeitet." );
        } else {
            statusField.showSuccess( "Keine Daten verarbeitet." );
        }
    }

    private void importCSV(File csvfile) {
        CSV csv;
        try {
            csv = new CSV(
                new dataformat.csv.Reader( csvfile, Charset.forName( "Cp1252" ), new Parser( "\t", false )), true
            );
            ArrayList<ArrayList<String>> dataset = csv.getDataset();
            for ( ArrayList<String> entry : dataset ) {
                csv_patienten.add( Patient.fromADG( entry.toArray( new String[0] )));
            }
            activateUI();
        } catch (IOException e) {
            jButtonADG.setEnabled(false);
            jButtonADGSelect.setEnabled(false);
            jButtonADGNew.setEnabled(false);
            jButtonDeleteRight.setEnabled(false);
            jButtonBackRight.setEnabled(false);
            statusField.showError( e.getMessage() );
            mainframe.statusField.showError("Lesen der Datei fehlgeschlagen (I/O Fehler)");
            System.err.println( e.toString() );
        }
    }

    private void activateUI() {
        for (int i = 0; i < ADG_FIELDS.size(); ++i) {
            table.setValueAt( ADG_FIELDS.get(i).getUIName(), i, 1 );
        }
        jButtonNextLeftActionPerformed(null);
        jButtonNextRightActionPerformed(null);
        jButtonNextRight.setEnabled(true);
    }

    private void mapTableData(Patient patient, int tableColumn) {
        for (int i = 0; i < ADG_FIELDS.size(); ++i) {
            table.setValueAt( patient.getFormattedValue( ADG_FIELDS.get( i )), i, tableColumn);
            coloringTable(i);
        }
    }

    private void compareRight() {
        mapTableData( csv_patienten.get( rightindex ), 2 );
        jLabelRight.setText("Nr.: " + rightindex);
    }

    private void compareLeft() {
        mapTableData( mainframe.getPatients().get( leftindex ), 0 );
        jLabelLeft.setText("Nr.: " + leftindex);
    }

    private void coloringTable(int index) {
        try {
            if ( table.getValueAt( index, 0 ).equals( table.getValueAt( index, 2 ))) {
                colorArray[index][0] = ColorLibrary.LIGHT_GREEN;
                colorArray[index][2] = ColorLibrary.LIGHT_GREEN;
            } else {
                colorArray[index][0] = ColorLibrary.LIGHT_RED;
                colorArray[index][2] = ColorLibrary.LIGHT_RED;
            }
        } catch (NullPointerException e) {}
        jTable1.repaint();
    }

