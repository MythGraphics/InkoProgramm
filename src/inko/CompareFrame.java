    class ColoringCellRenderer extends DefaultTableCellRenderer {
        @Override
        public Component getTableCellRendererComponent(
            JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int col
        ) {
            super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, col);
            setHorizontalAlignment(CENTER);
            if (( colorArray != null ) && ( colorArray[row][col] != null )) {
                setBackground( getColor( colorArray[row][col], row ));
            } else {
                setBackground( getDefaultColor( row ));
            }
            return this;
        }
        private Color getDefaultColor(int row) {
            if ( row%2 == 0 ) {
                return Color.WHITE;
            }
            return ColorLibrary.LIGHT_GRAY;
        }
        private Color getColor(Color color, int row) {
            if ( row%2 == 0 ) {
                return color.brighter();
            }
            return color;
        }
    }

