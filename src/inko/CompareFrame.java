    private void jButtonADGSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonADGSelectActionPerformed
        transfereID();
        int[] rows = jTable1.getSelectedRows();
        List<PatientField> list = new ArrayList<>();
        for (int i = 0; i < rows.length; ++i) {
            // generiert das Pattern-Feld der zu aktualisierenden Werte
            list.add( PatientField.ADG_FIELDS.get( i ));
        }
        int linesChanged = mainframe.pio.updatePatient( csv_patienten.get(rightindex), list );
        csv_patienten.remove(rightindex);
        --rightindex;
        jButtonNextRightActionPerformed(null);
        showStatus(linesChanged);
    }//GEN-LAST:event_jButtonADGSelectActionPerformed

