    private void jCreateArtikelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCreateArtikelButtonActionPerformed
        if (artikelFrame == null) {
            artikelFrame = new ArtikelFrame(this);
        }
        String md5sum;
        char[] password;
        // Passwort-Eingabe holen
        JPasswordField passfield = new JPasswordField();
        // Fokus-Logik hinzufügen
        passfield.addAncestorListener( new AncestorListener() {
            @Override
            public void ancestorAdded(AncestorEvent event) {
                // Fordert den Fokus an, sobald das JPasswordField sichtbar wird
                passfield.requestFocusInWindow();
            }
            @Override
            public void ancestorRemoved(AncestorEvent event) {}
            @Override
            public void ancestorMoved(AncestorEvent event) {}
        });
        int okCxl = JOptionPane.showConfirmDialog(
            this, passfield, "Passwort eingeben", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE
        );
        if (okCxl == JOptionPane.OK_OPTION) {
            password = passfield.getPassword();
        } else {
            return;
        }
        // md5sum berechnen
        try {
            MessageDigest digest = MessageDigest.getInstance("MD5");
            byte[] input = String.valueOf(password).getBytes();
            byte[] hash = digest.digest(input);
            // in Hex-String konvertieren
            StringBuilder hexString = new StringBuilder();
            for (byte b : hash) {
                String hex = Integer.toHexString(0xff & b);
                if ( hex.length() == 1 ) {
                    hexString.append('0');
                }
                hexString.append( hex.toUpperCase() );
            }
            md5sum = hexString.toString();
//          System.out.println( "Hash der Eingabe: " + md5sum ); // debug
        } catch (NoSuchAlgorithmException e) {
            System.err.println( e.getMessage() );
            return;
        }
        // Passwort-Hash aus DB holen
        String dbHash = String.valueOf( pio.getArtikelPasswordHash() );
//      System.out.println( "gespeicherter Hash (DB): " + dbHash ); // debug
        // Hashes vergleichen
        if ( dbHash.equalsIgnoreCase( md5sum )) {
            statusField.showSuccess("Passwort OK.");
            artikelFrame.setVisible(true);
        } else {
            statusField.showError("Passwort falsch.");
        }
    }//GEN-LAST:event_jCreateArtikelButtonActionPerformed

