    public static void main(String[] args) {
        if (args.length == 0) {
            showGUI();
            return;
        }
        switch (args[0]) {
            case "--dateformat":
                try {
                    System.out.println( DF.format( args[1] )); // fehlt der Wert "Tag", wird der letzte Tag des Monats gesetzt
                }
                catch (IllegalArgumentException | NoSuchElementException e) {
                    System.err.println( args[1] + " ungültig.");
                    System.err.println( "Datum unverständlich - Format unbekannt." );
                    System.err.println( e.getMessage() );
                }
                return;
            case "--help":
                printHelp();
                return;
            case "-?":
                printHelp();
                return;
            case "--version":
                System.out.println(NAME + " v" + VERSION);
                return;
        }
        for (int i = 0; i < args.length; ++i) {
            if ( args[i].startsWith( "--7z" )) {
                SEVENZIP = args[i].substring(4);
            }
            else if ( args[i].startsWith( "--oo" )) {
                OO = args[i].substring(4);
            }
            else if ( args[i].startsWith( "--server" )) {
                server = args[i].substring(8);
            }
            else if ( args[i].startsWith( "--user" )) {
                user = args[i].substring(6);
            }
            else if ( args[i].startsWith( "--pass" )) {
                pass = args[i].substring(6).toCharArray();
            }
            else if ( args[i].startsWith( "--ipPattern" )) {
                IP_PATTERN = args[i].substring(11);
            }
            else if ( args[i].equals( "--firststart" )) {
                String user;
                char[] pass;
                System.out.println("Starte Initiallisierung der Datenbank ...");
                System.out.print("Benutzer: ");
                try ( Scanner scanner = new Scanner( System.in )) {
                    user = scanner.next().trim();
                }
                System.out.print("Passwort: ");
                pass = System.console().readPassword();
                System.out.println("initiallisieren ...");
                DBio.firstRun(user, pass, server);
                System.out.println("... abgeschlossen.");
                return;
            }
            else if ( args[i].startsWith( "--updateDB" )) {
                System.out.println("Starte Update der Datenbank ...");
                DBio.updateDB(user, pass, server);
                System.out.println("... abgeschlossen.");
            }
            else if ( args[i].startsWith( "--info" )) {
                InfoFrame.main( new String[]{server} );
                return;
            }
            else if ( args[i].startsWith( "--ui" )) {
                showGUI();
                return;
            }
            else if ( args[i].startsWith( "--oldDBui" )) {
                inko.old.OldMainFrame.main(null);
                return;
            }
        }
        showGUI();
    }

