    private void setupEnterTraversal(JTable table) {
        // eine neue Action für die Enter-Taste definieren
        InputMap inputMap = table.getInputMap(JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        ActionMap actionMap = table.getActionMap();

        // KeyStroke für Enter abfangen
        KeyStroke enter = KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0);
        inputMap.put(enter, "selectNextColumnCell"); // interner Namen für "nächste Zelle"

        // eigener Action-Handler für mehr Kontrolle
        actionMap.put("selectNextColumnCell", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int row = table.getSelectedRow();
                int col = table.getSelectedColumn();

                // wenn wir in der Eingabe-Spalte (1) sind:
                if ( col == 1 ) {
                    // falls wir in der letzten Zeile sind, zur ersten Zeile
                    if ( row == table.getRowCount()-1 ) {
                        table.changeSelection(1, 1, false, false);
                    } else {
                        // eine Zeile runter
                        table.changeSelection(row+1, 1, false, false);
                    }
                    // sofort den Edit-Modus für die neue Zelle starten
                    table.editCellAt( table.getSelectedRow(), 1 );
                    Component editor = table.getEditorComponent();
                    if (editor != null) {
                        editor.requestFocus();
                    }
                }
            }
        });
    }

    private JTable getJTable() {
        patientTableModel = new PatientTableModel();
        JTable jTable = new JTable(patientTableModel) {
            private final PatientTableCellRenderer customRenderer = new PatientTableCellRenderer();
            @Override
            public TableCellRenderer getCellRenderer(int row, int column) {
                return customRenderer;
            }
            @Override
            public TableCellEditor getCellEditor(int row, int column) {
                if (column == 1) {
                    // PatientField für die aktuelle Zeile aus der Liste des Modells holen
                    PatientField field = UI_FIELDS.get(row);
                    // wähle den Editor basierend auf dem Feldtyp im Enum
                    if (field.getType() == LocalDate.class) {
                        return new LocalDateCellEditor();
                    } else if (field == TYP) {
                        return new DefaultCellEditor( new JComboBox<>( InkoType.values() ));
                    }
                    // ... weitere Spezialfälle ...
                }
                return super.getCellEditor(row, column);
            }
        };
        setupEnterTraversal(jTable);
        jTable.putClientProperty("terminateEditOnFocusLost", Boolean.TRUE);
        return jTable;
    }

