    private void deleteEntry() {
        int option = JOptionPane.showConfirmDialog(
            this,
            "Patient unwiderruflich löschen?",
            "Absicht?",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );
        if (option != JOptionPane.YES_OPTION) {
            return;
        }
        Patient p = (Patient) patientComboBox.getSelectedItem();
        boolean success = pio.deletePatient( p.getId() );
        if (success) {
            patientComboBox.removeItem(p);
            statusField.showSuccess("Eintrag \"" + p.toString() + "\" gelöscht.");
        }
    }

    private void setTypeUI() {
        switch ( patientTableModel.getPatient().getType() ) {
            case ABLEITEND:     aRadioButton.setSelected(true); break;
            case SAUGEND:       sRadioButton.setSelected(true); break;
            case SAUGEND_KIND:  kRadioButton.setSelected(true); break;
        }
    }

    protected void saveEntry(Patient patient) {
        if (patient == null) {
            statusField.showError("Kein Patient zum Speichern geladen.");
            return;
        }
        else if ( !patient.isModified() ) {
            statusField.showProblem("Keine Änderung an Patient erkannt. Speichern nicht erforderlich.");
            return;
        }
        try {
            pio.savePatient(patient);
            statusField.showSuccess( "Patient " + patient.getFullName() + " erfolgreich in Datenbank gespeichert." );
            patientComboBoxModel.updateSelectedDisplay(patientComboBox);
        } catch (Exception e) {
            statusField.showError( "Fehler beim Speichern: " + e.getMessage() );
            e.printStackTrace();
        }
    }

