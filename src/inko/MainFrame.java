    protected void makeHtml(ActionEvent evt, Document doc) {
        HTMLMaker htmlmaker = new HTMLMaker( doc.getFileName(), outpath+doc.getFileName() );
        try {
            switch (doc) {
                case PATIENTENLISTE:
                    htmlmaker.makePatientList( patientComboBoxModel.getList() );
                    break;
                case HIMILISTE:
                    htmlmaker.makeArtikelList( patientComboBoxModel.getList(), pio.getArtikelList() );
                    break;
                case HIMILISTE2:
                    htmlmaker.makeArtikelList2( patientComboBoxModel.getList() );
                    break;
                case DECKBLATT:
                    htmlmaker.makeFrontpage( patientTableModel.getPatient() );
                    break;
                case BERATUNGSBOGEN:
                    htmlmaker.makeSignableDocument( patientTableModel.getPatient(), BERATUNG );
                    break;
                case BINDUNGSERKLÄRUNG:
                    htmlmaker.makeSignableDocument( patientTableModel.getPatient(), BINDUNG );
                    break;
                case MEHRKOSTENERKLÄRUNG:
                    htmlmaker.makeSignableDocument( patientTableModel.getPatient(), MEHRKOSTEN );
                    break;
                case LIEFERLISTE:
                    htmlmaker.makeDeliveryList( patientComboBoxModel.getList() );
                    break;
                case FÄLLIGKEITSLISTE:
                    htmlmaker.makeExpiringList( patientComboBoxModel.getList(), HTMLMaker.EXPIRINGLIST );
                    break;
                case FÄLLIGKEITSLISTE_RX:
                    htmlmaker.makeExpiringList( patientComboBoxModel.getList(), HTMLMaker.EXPIRINGLIST_RX );
                    break;
                case FÄLLIGKEITSLISTE_BIND:
                    htmlmaker.makeExpiringList( patientComboBoxModel.getList(), HTMLMaker.EXPIRINGLIST_BIND );
                    break;
            }
        } catch (IOException e) {
            statusField.showError(HTMLMaker.IOERROR);
            e.printStackTrace();
        }
    }

