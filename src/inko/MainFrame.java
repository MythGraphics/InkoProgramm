    protected void loadEntry(Patient patient) {
        if (patient == null) {
            return;
        }
        Patient currentPatient = patient;
        if ( patient.getId() != -1 ) {
            currentPatient = pio.getPatientById( patient.getId() ); // lädt die aktuellen Patienten-Daten aus der DB
        }

        if (currentPatient == null) {
            System.err.println("loadEntry: currentPatient ist NULL");
            return;
        }

        patientTableModel.setPatient(currentPatient);
        patientArtikelListModel.setPatient(currentPatient);
        loadEntryUpdateUI(currentPatient);
    }

    protected void loadEntryUpdateUI(Patient patient) {
        liefernCheckBox.setSelected( patient.toDeliver() );
        pauseCheckBox.setSelected( patient.isPaused() );

        freiCheckBox.setSelected( patient.isCoPaymentFree() );
        freiCheckBoxActionPerformed(null);
        jYearChooser.setValue( patient.getCoPaymentFreeUntil().getYear() );

        insurenceTextField.setText( patient.getHealthInsurer() );
        setTypeUI();
        typeChangeActionPerformed();

        if ( patient.hasSignatureData() ) {
            if ( patient.getSignature(BERATUNG) != null && patient.getSignature(BERATUNG).getSign() != null ) {
                BeratungsbogenButton.setBackground(GREEN);
            } else {
                BeratungsbogenButton.setBackground(RED);
            }
            if ( patient.getSignature(BINDUNG) != null && patient.getSignature(BINDUNG).getSign() != null ) {
                BindungserklärungButton.setBackground(GREEN);
            } else {
                BindungserklärungButton.setBackground(RED);
            }
            if ( patient.getSignature(MEHRKOSTEN) != null && patient.getSignature(MEHRKOSTEN).getSign() != null ) {
                MehrkostenerklärungButton.setBackground(GREEN);
            } else {
                MehrkostenerklärungButton.setBackground(RED);
            }
        }

        refreshArtikelComboBox();
        jTable1.requestFocus();
        jTable1.editCellAt(1, 1);

        patient.setModified(false);
    }

